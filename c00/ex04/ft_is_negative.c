#include <unistd.h>

void	ft_is_negative(int c)

{
	char	posit;
	char	neg;

	posit = 'P';
	neg = 'N';
	if (c >= 0)
	{
		write(1, &posit, 1);
	}
	else
	{
		write(1, &neg, 1);
	}
}
